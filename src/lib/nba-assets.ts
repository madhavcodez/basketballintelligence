// NBA CDN asset URL helpers

/**
 * Get player headshot URL from NBA CDN.
 * @param personId - NBA person_id (numeric)
 * @returns URL string for 1040x760 headshot PNG
 */
export function playerHeadshotUrl(personId: number | string): string {
  return `https://cdn.nba.com/headshots/nba/latest/1040x760/${personId}.png`;
}

/**
 * Get team logo URL from NBA CDN.
 * @param teamId - NBA team_id (numeric)
 * @returns URL string for team logo SVG
 */
export function teamLogoUrl(teamId: number | string): string {
  return `https://cdn.nba.com/logos/nba/${teamId}/global/L/logo.svg`;
}

